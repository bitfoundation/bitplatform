    /// Custom CSS classes/styles for the content of the BitCallout.
    /// </summary>
    public string? Content { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the overlay of the BitCallout.
    /// </summary>
    public string? Overlay { get; set; }
}
