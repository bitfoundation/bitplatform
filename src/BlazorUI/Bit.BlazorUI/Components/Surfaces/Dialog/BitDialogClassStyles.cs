namespace Bit.BlazorUI;

public class BitDialogClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitDialog.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the overlay of the BitDialog.
    /// </summary>
    public string? Overlay { get; set; }
    
    /// <summary>
    /// Custom CSS classes/styles for the container of the BitDialog.
    /// </summary>
    public string? Container { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header of the BitDialog.
    /// </summary>
    public string? Header { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the body of the BitDialog.
    /// </summary>
    public string? Body { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the title of the BitDialog.
    /// </summary>
    public string? Title { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the message of the BitDialog.
    /// </summary>
    public string? Message { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the buttons container of the BitDialog.
    /// </summary>
    public string? ButtonsContainer { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the loading spinner of the ok button of the BitDialog.
    /// </summary>
    public string? Spinner { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the ok button of the BitDialog.
    /// </summary>
    public string? OkButton { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the cancel button of the BitDialog.
    /// </summary>
    public string? CancelButton { get; set; }
}
