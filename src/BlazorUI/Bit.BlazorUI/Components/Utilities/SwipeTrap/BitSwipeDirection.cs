namespace Bit.BlazorUI;

/// <summary>
/// The direction in which the swipe trap triggers.
/// </summary>
public enum BitSwipeDirection
{
    /// <summary>
    /// Swipe to right direction.
    /// </summary>
    Right = 0,

    /// <summary>
    /// Swipe to left direction.
    /// </summary>
    Left = 1,

    /// <summary>
    /// Swipe to top direction.
    /// </summary>
    Top = 2,

    /// <summary>
    /// Swipe to bottom direction.
    /// </summary>
    Bottom = 3,
}
