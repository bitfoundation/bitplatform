namespace Bit.BlazorUI;

/// <summary>
/// Determines the severity of the content that controls the colors of the rendered element(s).
/// </summary>
public enum BitSeverity
{
    /// <summary>
    /// Info styled severity.
    /// </summary>
    Info,

    /// <summary>
    /// Success styled severity.
    /// </summary>
    Success,

    /// <summary>
    /// Warning styled severity.
    /// </summary>
    Warning,

    /// <summary>
    /// SevereWarning styled severity.
    /// </summary>
    SevereWarning,

    /// <summary>
    /// Error styled severity.
    /// </summary>
    Error
}
