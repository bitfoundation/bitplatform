    /// </summary>
    public string? ItemImageContainer { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the image wrapper of each item of the BitChoiceGroup.
    /// </summary>
    public string? ItemImageWrapper { get; set; }

