    /// Custom CSS classes/styles for the icon wrapper of each item of the BitChoiceGroup.
    /// </summary>
    public string? ItemIconWrapper { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the icon of each item of the BitChoiceGroup.
    /// </summary>
    public string? ItemIcon { get; set; }

