namespace Bit.BlazorUI;

public class BitSpinButtonClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the spin button.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's label container.
    /// </summary>
    public string? LabelContainer { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's icon.
    /// </summary>
    public string? Icon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's label.
    /// </summary>
    public string? Label { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's container.
    /// </summary>
    public string? Container { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's decrement button container.
    /// </summary>
    public string? DecrementButton { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's decrement icon wrapper.
    /// </summary>
    public string? DecrementIconWrapper { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's decrement icon.
    /// </summary>
    public string? DecrementIcon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's input.
    /// </summary>
    public string? Input { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's buttons wrapper in Compact mode.
    /// </summary>
    public string? CompactButtonsWrapper { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's increment button icon.
    /// </summary>
    public string? IncrementButton { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's increment icon wrapper.
    /// </summary>
    public string? IncrementIconWrapper { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spin button's increment icon.
    /// </summary>
    public string? IncrementIcon { get; set; }
}
