namespace Bit.BlazorUI;

public enum BitSpinButtonMode
{
    /// <summary>
    /// Spinning buttons render as a compact stack at the end of the input.
    /// </summary>
    Compact,

    /// <summary>
    /// Spinning buttons render inlined at the end of the input.
    /// </summary>
    Inline,

    /// <summary>
    /// Spinning buttons render at the start and end of the input.
    /// </summary>
    Spread
}
