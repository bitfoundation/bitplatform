namespace Bit.BlazorUI;

public class BitDropMenuClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitDropMenu.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the opened callout state of the BitDropMenu.
    /// </summary>
    public string? Opened { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the button of the BitDropMenu.
    /// </summary>
    public string? Button { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the icon of the BitDropMenu.
    /// </summary>
    public string? Icon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the text of the BitDropMenu.
    /// </summary>
    public string? Text { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the chevron-down icon of the BitDropMenu.
    /// </summary>
    public string? ChevronDown { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the overlay of the BitDropMenu.
    /// </summary>
    public string? Overlay { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the callout of the BitDropMenu.
    /// </summary>
    public string? Callout { get; set; }
}
