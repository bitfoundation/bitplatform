namespace Bit.BlazorUI;

public class BitNavBarClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitNavBar.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the container of the items of the BitNavBar.
    /// </summary>
    public string? Container { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the item of the BitNavBar.
    /// </summary>
    public string? Item { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the item icon of the BitNavBar.
    /// </summary>
    public string? ItemIcon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the item text of the BitNavBar.
    /// </summary>
    public string? ItemText { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the selected item of the BitNavBar.
    /// </summary>
    public string? SelectedItem { get; set; }
}
