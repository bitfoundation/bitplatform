namespace Bit.BlazorUI;

public class BitPivotClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitPivot.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header of the BitPivot.
    /// </summary>
    public string? Header { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the items body of the BitPivot.
    /// </summary>
    public string? Body { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header item of the BitPivot.
    /// </summary>
    public string? HeaderItem { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the selected item of the BitPivot.
    /// </summary>
    public string? SelectedItem { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header item content of the BitPivot.
    /// </summary>
    public string? HeaderItemContent { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header icon container of the BitPivot.
    /// </summary>
    public string? HeaderIconContainer { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header icon of the BitPivot.
    /// </summary>
    public string? HeaderIcon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header text of the BitPivot.
    /// </summary>
    public string? HeaderText { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the header item count of the BitPivot.
    /// </summary>
    public string? HeaderItemCount { get; set; }
}
