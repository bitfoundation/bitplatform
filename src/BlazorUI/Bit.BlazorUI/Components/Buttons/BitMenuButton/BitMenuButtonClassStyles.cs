namespace Bit.BlazorUI;

public class BitMenuButtonClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitMenuButton.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the callout container of the BitMenuButton.
    /// </summary>
    public string? Callout { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the chevron down of the BitMenuButton.
    /// </summary>
    public string? ChevronDown { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the icon of the BitMenuButton.
    /// </summary>
    public string? Icon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for each item of the BitMenuButton.
    /// </summary>
    public string? ItemButton { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for each item icon of the BitMenuButton.
    /// </summary>
    public string? ItemIcon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for each item text of the BitMenuButton.
    /// </summary>
    public string? ItemText { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the overlay of the BitMenuButton.
    /// </summary>
    public string? Overlay { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the text of the BitMenuButton.
    /// </summary>
    public string? Text { get; set; }
}
