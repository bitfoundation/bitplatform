namespace Bit.BlazorUI;

public class BitButtonClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitButton.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the icon of the BitButton.
    /// </summary>
    public string? Icon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the internal container of the BitButton.
    /// </summary>
    public string? Container { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the primary section of the BitButton.
    /// </summary>
    public string? Primary { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the secondary section of the BitButton.
    /// </summary>
    public string? Secondary { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the loading container of the BitButton.
    /// </summary>
    public string? LoadingContainer { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the spinner of the BitButton.
    /// </summary>
    public string? Spinner { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the loading label of the BitButton.
    /// </summary>
    public string? LoadingLabel { get; set; }
}
