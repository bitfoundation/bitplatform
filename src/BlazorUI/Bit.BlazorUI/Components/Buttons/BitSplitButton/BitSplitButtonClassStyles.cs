namespace Bit.BlazorUI;

public class BitSplitButtonClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the callout container of the BitSplitButton.
    /// </summary>
    public string? Callout { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the chevron down button of the BitSplitButton.
    /// </summary>
    public string? ChevronDownButton { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the chevron down icon of the BitSplitButton.
    /// </summary>
    public string? ChevronDownIcon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for each item of the BitSplitButton.
    /// </summary>
    public string? ItemButton { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for each item icon of the BitSplitButton.
    /// </summary>
    public string? ItemIcon { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for each item text of the BitSplitButton.
    /// </summary>
    public string? ItemText { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the overlay of the BitSplitButton.
    /// </summary>
    public string? Overlay { get; set; }
}
