namespace Bit.BlazorUI;

public class BitProgressBarClassStyles
{
    /// <summary>
    /// Custom CSS classes/styles for the root element of the BitProgressBar.
    /// </summary>
    public string? Root { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the label of the BitProgressBar.
    /// </summary>
    public string? Label { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the percent number of the BitProgressBar.
    /// </summary>
    public string? PercentNumber { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the bar container of the BitProgressBar.
    /// </summary>
    public string? BarContainer { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the track of the BitProgressBar.
    /// </summary>
    public string? Track { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the bar of the BitProgressBar.
    /// </summary>
    public string? Bar { get; set; }

    /// <summary>
    /// Custom CSS classes/styles for the description of the BitProgressBar.
    /// </summary>
    public string? Description { get; set; }
}
